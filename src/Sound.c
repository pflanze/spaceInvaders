void Sound_stop_all(const struct Sound *sp) {
	if(soundState1.sound == sp){
		Timer1A_Stop();
		soundStateInit(&soundState1);
	}
	
	if(soundState2.sound == sp){
		Timer2A_Stop();
		soundStateInit(&soundState2);
	}
}

